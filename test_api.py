def run():
    print("Calling API function...")
    req = MindmapRequest(markdown_text=markdown_data)
    res = generate_mindmap(req)
    
    # 提取 base64 数据部分并解码保存为 PNG
    base64_str = res.image_base64.split(",")[1]
    out_path = "api_output.png"
    
    with open(out_path, "wb") as f:
        f.write(base64.b64decode(base64_str))
        
    print(f"Successfully generated mindmap using the API plugin! Saved to {out_path}")

if __name__ == "__main__":
    run()
